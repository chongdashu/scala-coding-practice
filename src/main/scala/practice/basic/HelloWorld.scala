package main.scala.practice.basic

/**
 * A simple Hello World application using Scala.
 * This a basic example demonstrating:
 * 		HelloWorld is an object, not a class.
 * 		Return type is Unit
 * 		Note the "=" operation for function definitions.
 * 
 * @author Chong-U Lim
 */
object HelloWorld {
    def main(args : Array[String]) : Unit = {
        println("Hello, World!");    
    }
    
}